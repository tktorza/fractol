#include "libft.h"

char	*ft_change(char *str, char c, char d)
{
	int		i;

	i = 0;
	while (str[i])
	{
		if (str[i] == c)
			str[i] = d;
		i++;
	}
	str[i] = '\0';
	return (str);
}
