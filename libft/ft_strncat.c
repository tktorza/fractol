#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t y;

	y = 0;
	i = ft_strlen(s1);
	while (n > 0)
	{
		s1[i] = s2[y];
		i++;
		y++;
		n--;
	}
	s1[i] = '\0';
	return (s1);
}
