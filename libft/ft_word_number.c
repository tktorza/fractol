#include "libft.h"

size_t		ft_word_number(char const *s, char c)
{
	size_t i;
	size_t car;

	i = 0;
	car = 0;
	if (s[i] == c)
		i++;
	while (s[i])
	{
		if (s[i] != c && (s[i + 1] == c || s[i + 1] == '\0'))
			car++;
		i++;
	}
	return (car);
}
